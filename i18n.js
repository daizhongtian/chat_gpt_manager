  function normalizeLanguage(code) {
    const value = String(code || "").trim();
    if (!value || value === "auto") {
      return "auto";
    }

    const lower = value.toLowerCase();
    if (/^zh-(tw|hk|mo|hant)/i.test(value) || lower === "zh-tw") {
      return "zh-TW";
    }
    if (/^zh/i.test(value)) {
      return "zh-CN";
    }
    if (/^pt/i.test(value)) {
      return "pt-BR";
    }

    const exact = LANGUAGE_OPTIONS.find((language) => language.code.toLowerCase() === lower);
    if (exact) {
      return exact.code;
    }

    const base = lower.split("-")[0];
    const baseMatch = LANGUAGE_OPTIONS.find((language) => language.code.toLowerCase().split("-")[0] === base);
    return baseMatch ? baseMatch.code : DEFAULT_LANGUAGE;
  }

  function getBrowserLanguage() {
    const languages = navigator.languages && navigator.languages.length ? navigator.languages : [navigator.language];
    return normalizeLanguage(languages.find(Boolean) || DEFAULT_LANGUAGE);
  }

  function resolveLanguage(preference) {
    const normalized = normalizeLanguage(preference);
    return normalized === "auto" ? getBrowserLanguage() : normalized;
  }

  function translate(key, params = {}, language = DEFAULT_LANGUAGE) {
    const resolved = resolveLanguage(language);
    const text = (MESSAGES[resolved] && MESSAGES[resolved][key]) || EN[key] || key;
    return String(text).replace(/\{(\w+)\}/g, (match, name) => {
      return Object.prototype.hasOwnProperty.call(params, name) ? String(params[name]) : match;
    });
  }

  function formatNumber(value, language = DEFAULT_LANGUAGE) {
    return new Intl.NumberFormat(getNumberLocale(language)).format(Number(value || 0));
  }

  function getNumberLocale(language = DEFAULT_LANGUAGE) {
    const resolved = resolveLanguage(language);
    return resolved === "zh-CN" ? "zh-Hans" : resolved;
  }

  function getDirection(language = DEFAULT_LANGUAGE) {
    return RTL_LANGUAGES.has(resolveLanguage(language)) ? "rtl" : "ltr";
  }

  function populateLanguageSelect(select, selectedValue) {
    if (!select) {
      return;
    }

    select.textContent = "";
    LANGUAGE_OPTIONS.forEach((language) => {
      const option = document.createElement("option");
      option.value = language.code;
      option.textContent = language.label;
      select.appendChild(option);
    });
    select.value = normalizeLanguage(selectedValue || "auto");
  }

  globalThis.ChatGPTCleanerI18n = {
    DEFAULT_LANGUAGE,
    LANGUAGE_OPTIONS,
    normalizeLanguage,
    resolveLanguage,
    getDirection,
    getNumberLocale,
    formatNumber,
    populateLanguageSelect,
    t: translate
  };
